#include <stdlib.h>
#include "libft.h"

void	*ft_memalloc(size_t size)
{
	unsigned char	*new;
	size_t			i;

	new = NULL;
	new = (unsigned char *)malloc(sizeof(char) * size);
	if (new)
	{
		i = 0;
		while (size--)
			new[i++] = 0;
	}
	return (new);
}
